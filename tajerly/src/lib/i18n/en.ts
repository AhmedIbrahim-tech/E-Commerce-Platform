/**
 * Deeply maps all leaf values to `string`,
 * so that Arabic (or any other locale) can use any string value.
 */
type DeepStringify<T> = {
    [K in keyof T]: T[K] extends Record<string, unknown> ? DeepStringify<T[K]> : string;
};

